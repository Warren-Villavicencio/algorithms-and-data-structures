texto = "Esto es un ejemplo de métodos de strings en Python"
print(texto.upper())  # Convierte a mayúsculas
print(texto.lower())  # Convierte a minúsculas
print(texto.split())  # Divide el string en una lista de palabras
print(texto.find("ejemplo"))  # Encuentra el índice de la primera ocurrencia de "ejemplo"